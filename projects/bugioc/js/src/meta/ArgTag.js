require('bugpack').context("*", function(bugpack) {

    //-------------------------------------------------------------------------------
    // BugPack
    //-------------------------------------------------------------------------------

    var Class           = bugpack.require('Class');
    var Tag      = bugpack.require('bugmeta.Tag');


    //-------------------------------------------------------------------------------
    // Declare Class
    //-------------------------------------------------------------------------------

    /**
     * @class
     * @extends {Tag}
     */
    var ArgTag = Class.extend(Tag, {

        _name: "bugioc.ArgTag",


        //-------------------------------------------------------------------------------
        // Constructor
        //-------------------------------------------------------------------------------

        /**
         * @constructs
         * @private
         */
        _constructor: function() {

            this._super(ArgTag.TYPE);


            //-------------------------------------------------------------------------------
            // Private Properties
            //-------------------------------------------------------------------------------

            /**
             * @private
             * @type {string}
             */
            this.argRef     = null;

            /**
             * @private
             * @type {*}
             */
            this.argValue   = null;
        },


        //-------------------------------------------------------------------------------
        // Getters and Setters
        //-------------------------------------------------------------------------------

        /**
         * @return {string}
         */
        getArgRef: function() {
            return this.argRef;
        },

        /**
         * @return {*}
         */
        getArgValue: function() {
            return this.argValue;
        },


        //-------------------------------------------------------------------------------
        // Class Methods
        //-------------------------------------------------------------------------------

        /**
         * @param {string} argRef
         * @return {ArgTag}
         */
        ref: function(argRef) {
            this.argRef = argRef;
            return this;
        },

        /**
         * @param {*} argValue
         * @return {ArgTag}
         */
        value: function(argValue) {
            this.argValue = argValue;
            return this;
        }
    });


    //-------------------------------------------------------------------------------
    // Static Properties
    //-------------------------------------------------------------------------------

    /**
     * @static
     * @const {string}
     */
    ArgTag.TYPE = "Arg";


    //-------------------------------------------------------------------------------
    // Static Methods
    //-------------------------------------------------------------------------------

    /**
     * @static
     * @return {ArgTag}
     */
    ArgTag.arg = function() {
        return new ArgTag();
    };


    //-------------------------------------------------------------------------------
    // Exports
    //-------------------------------------------------------------------------------

    bugpack.export('bugioc.ArgTag', ArgTag);
});
