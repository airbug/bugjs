var bugpack         = require('bugpack').context();


//-------------------------------------------------------------------------------
// BugPack
//-------------------------------------------------------------------------------

var Class           = bugpack.require('Class');
var Annotation      = bugpack.require('bugmeta.Annotation');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var ArgAnnotation = Class.extend(Annotation, {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    _constructor: function() {

        this._super("Arg");


        //-------------------------------------------------------------------------------
        // Private Properties
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {string}
         */
        this.argRef     = null;

        /**
         * @private
         * @type {*}
         */
        this.argValue   = null;
    },


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    getArgRef: function() {
        return this.argRef;
    },

    /**
     * @return {*}
     */
    getArgValue: function() {
        return this.argValue;
    },


    //-------------------------------------------------------------------------------
    // Class Methods
    //-------------------------------------------------------------------------------

    /**
     * @param {string} argRef
     * @return {ArgAnnotation}
     */
    ref: function(argRef) {
        this.argRef = argRef;
        return this;
    },

    /**
     * @param {*} argValue
     * @return {ArgAnnotation}
     */
    value: function(argValue) {
        this.argValue = argValue;
        return this;
    }
});


//-------------------------------------------------------------------------------
// Static Methods
//-------------------------------------------------------------------------------

/**
 * @static
 * @return {ArgAnnotation}
 */
ArgAnnotation.arg = function() {
    return new ArgAnnotation();
};


//-------------------------------------------------------------------------------
// Exports
//-------------------------------------------------------------------------------

bugpack.export('bugioc.ArgAnnotation', ArgAnnotation);
