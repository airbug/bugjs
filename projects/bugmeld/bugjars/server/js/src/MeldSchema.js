var bugpack     = require('bugpack').context();
var mongoose    = require('mongoose');


//-------------------------------------------------------------------------------
// Simplify References
//-------------------------------------------------------------------------------

var Schema      = mongoose.Schema;
var Mixed       = mongoose.Schema.Types.Mixed;


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var MeldSchema = new Schema({
    meld: {
        type: Mixed,
        required: true
    },
    callManagerUuid: {
        type: String,
        require: true,
        unique: true
    }
});

MeldSchema.index({
    callManagerUuid: 1
});


//-------------------------------------------------------------------------------
// Exports
//-------------------------------------------------------------------------------

bugpack.export('bugmeld.MeldSchema', MeldSchema);
