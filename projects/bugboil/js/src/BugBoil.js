var bugpack = require('bugpack');


//-------------------------------------------------------------------------------
// BugPack
//-------------------------------------------------------------------------------

bugpack.declare('BugBoil');

var Each = bugpack.require('Each');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var BugBoil = {};


//-------------------------------------------------------------------------------
// Static Methods
//-------------------------------------------------------------------------------

/**
 * @param {Array<*>} data
 * @return {Parallel}
 */
BugBoil.each = function(data, iteratorMethod) {
    return new Each(data, iteratorMethod);
};


//-------------------------------------------------------------------------------
// Export
//-------------------------------------------------------------------------------

bugpack.export(BugBoil);
