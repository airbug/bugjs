var ForEach = bugpack.require('ForEach');


//-------------------------------------------------------------------------------
// Declare Class
//-------------------------------------------------------------------------------

var BugBoil = {};


//-------------------------------------------------------------------------------
// Static Methods
//-------------------------------------------------------------------------------

/**
 * @param {Array<*>} data
 * @return {Parallel}
 */
BugBoil.$foreach = function(data, iteratorMethod) {
    return new ForEach(data, iteratorMethod);
};


