//-------------------------------------------------------------------------------
// Common Modules
//-------------------------------------------------------------------------------

var bugpack                 = require('bugpack').context(module);


//-------------------------------------------------------------------------------
// BugPack
//-------------------------------------------------------------------------------

var MigrationApplication    = bugpack.require('bugmigrate.MigrationApplication');


//-------------------------------------------------------------------------------
// Script
//-------------------------------------------------------------------------------

var application  = new MigrationApplication();
application.start(function(throwable) {
    if (!throwable) {
        process.exit();
    } else {
        process.exit(1);
    }
});
